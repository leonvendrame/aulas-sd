.INCLUDE "m328Pdef.inc"

.EQU LED0 = PB0
.EQU LED1 = PB1
.EQU LED2 = PB2
.EQU LED3 = PB3
.EQU BTN0 = PB4

START:
	SBI DDRB, LED0
	SBI	DDRB, LED1
	SBI DDRB, LED2
	SBI DDRB, LED3
	CBI DDRB, BTN0
	SBI PORTB, BTN0
	SBI PORTB, LED3

MAIN:
	SBIC PINB, BTN0
	RJMP MAIN
	
	SBIC PORTB, LED0
	RJMP RESET_LEDS
	RJMP TURN_ON_NEXT_LED

	WAITING_BTN0:
		SBIS PINB, BTN0
		RJMP WAITING_BTN0
	
	CALL DELAY
	RJMP MAIN

TURN_ON_NEXT_LED:
	IN 	R16, PORTB
	ANDI R17, 0x0F
	LSR R17
	ANDI R18, 0xF0
	OR 	R17, R18
	OUT PORTB, R17
	RJMP WAITING_BTN0

RESET_LEDS:
	CBI PORTB, LED0
	SBI PORTB, LED3
	RJMP WAITING_BTN0

DELAY:
	DEC R0
 	BRNE DELAY
 	DEC R1
 	BRNE DELAY
	RET
